#this example shows the basics of if and else statements
if spam == 1:
    #notice how the body of the loop is indented
	print('Hello')
#elif is short for "else, if"
elif spam == 2:
	print('Howdy')
#the else portion is executed if neither the if, nor elif portions are executed
else:
	print('Greetings!')
    
